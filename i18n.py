def normalise_language(value: str | None) -> str:
    value = (value or DEFAULT_LANGUAGE).strip().lower()
    return value if value in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE


def language_label(code: str) -> str:
    return LANGUAGE_LABELS[normalise_language(code)]


def language_from_label(label: str) -> str:
    for code, value in LANGUAGE_LABELS.items():
        if label == value:
            return code
    return DEFAULT_LANGUAGE


def translate_text(text: Any, language: str) -> Any:
    if not isinstance(text, str) or normalise_language(language) == "fr":
        return text
    exact = EXACT_EN.get(text)
    if exact is not None:
        return exact
    translated = text
    for source, target in sorted(FRAGMENTS_EN.items(), key=lambda item: len(item[0]), reverse=True):
        translated = translated.replace(source, target)
    return translated


def scoring_label(key: str, language: str) -> str:
    labels = SCORING_LABELS_EN if normalise_language(language) == "en" else SCORING_LABELS_FR
    return labels.get(str(key), str(key))


def profile_values(kind: str, language: str) -> tuple[str, ...]:
    language = normalise_language(language)
    return tuple(item[language] for item in PROFILE_OPTIONS[kind].values())


def profile_label(kind: str, code: str, language: str) -> str:
    language = normalise_language(language)
    options = PROFILE_OPTIONS[kind]
    if code not in options:
        code = next(iter(options))
    return options[code][language]


def profile_code(kind: str, display_or_code: str) -> str:
    options = PROFILE_OPTIONS[kind]
    if display_or_code in options:
        return display_or_code
    for code, labels in options.items():
        if display_or_code in labels.values():
            return code
    return next(iter(options))
